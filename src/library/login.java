    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if( b == 1)
        {
            if(jTextField1.getText().equals("admin") && jTextField2.getText().equals("admin"))
            {
               JOptionPane.showMessageDialog(null,"Login Successfull.....");
                
                setVisible(false);
                new Intro(b,null).setVisible(true);
            }
            else
            {
               JOptionPane.showMessageDialog(null,"Incorrect username or Password....."); 
            }
        }
        else
        {
         try
        {
            stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("Select* From login where user = '"+jTextField1.getText()+"' ");
        if(rs.next())
        {
            if(rs.getString(6).equals(jTextField1.getText()) && rs.getString(7).equals(jTextField2.getText()))
            {
                
                JOptionPane.showMessageDialog(null,"Login Successfull.....");
                roll = rs.getString(1);
                setVisible(false);
                new Intro(b,roll).setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Password Does Not Exist.....");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Username Does Not Exist....."); 
        }
        }
        catch(Exception e)
        {
            
        }   
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

